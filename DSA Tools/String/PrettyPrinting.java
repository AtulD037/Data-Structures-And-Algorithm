package String;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 4353.1274f;
        System.out.printf("Formatted Number is %.2f ",a);
        System.out.println();
        System.out.printf("Formatted Value of PI is %.3f",Math.PI);
        System.out.println();
        System.out.printf("My name is %s %s","Atul","Dhiman");

    }
}
