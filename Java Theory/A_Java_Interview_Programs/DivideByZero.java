package A_Java_Interview_Programs;

public class DivideByZero {
    public static void main(String[] args) {
        System.out.println("9.0/0 "+9.0/0);
        System.out.println("12.33f/0 "+12.33f/0);
        System.out.println("10/0.0 "+10/0.0);
        System.out.println("19.9999f/0 "+19.99f/0);
        System.out.println("0.0/0 "+0.0/0 );
        System.out.println("0.0/0.0 "+0.0/0.0);
        System.out.println("12.33/0.0 "+12.33/0.0);
    }
}
